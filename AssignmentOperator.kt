//Assign values to operand  a=b
fun main()
{
    var number1=12.5
    val number2=2.5

    number1+=number2
    println(number1);

    number1=12.5
    number1-=number2
    println(number1);

    number1=12.5
    number1*=number2
    println(number1);


    number1=12.5
    number1/=number2
    println(number1);

    number1=11.5
    number1%=number2
    println(number1);

}